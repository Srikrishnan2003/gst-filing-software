/**
 * Get SAC group information by code
 */
export function getSACGroupInfo(code: string): SACEntry | undefined {
    const groupCode = code.substring(0, 4);
    return SAC_GROUPS[groupCode];
}

/**
 * Get SAC service information by code
 */
export function getSACServiceInfo(code: string): SACEntry | undefined {
    if (code.length >= 6) {
        return SAC_SERVICES[code.substring(0, 6)];
    }
    return SAC_GROUPS[code.substring(0, 4)];
}
